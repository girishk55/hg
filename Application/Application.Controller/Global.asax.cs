            //      return f.SupportedMediaTypes.Any(v => v.MediaType == "text/xml");
            //  })
            //  .FirstOrDefault();

            //if (xmlFormatter != null)
            //{
            //    config.Formatters.Remove(xmlFormatter);
            //}
            Bootstrapper.Initialize();
        }
    }
}